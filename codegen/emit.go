func (g *Generator) emitLiterals() {
	if len(g.literals) == 0 {
		return
	}

	g.emit("")
	g.emit("; literals")

	for i, lit := range g.literals {
		g.emit(fmt.Sprintf("literal_%d:", i))

		if len(lit) > 0 {
			g.emit(fmt.Sprintf("    .byte %s", asmStringBytes(lit)))
		}
	}
}

func (g *Generator) emitStaticData() {
	g.emit("")
	g.emit("; static data")

	for _, frame := range g.frames {
		for _, sym := range frame.Symbols {
			g.emit(fmt.Sprintf("%s:", sym.Label))
			g.emitStaticValue(sym.Type)
		}

		if frame.Return != nil {
			g.emit(fmt.Sprintf("%s:", frame.Return.Label))
			g.emitStaticValue(frame.Return.Type)
		}
	}

	for _, sym := range g.forLoopTemps {
		g.emit(fmt.Sprintf("%s:", sym.Label))
		g.emitStaticValue(sym.Type)
	}
}

func (g *Generator) emitStaticValue(t ast.Type) {
	if t.IsArray {
		initialLen := 0

		g.emit(fmt.Sprintf("    .word %d", t.ArrayLen))
		g.emit(fmt.Sprintf("    .word %d", initialLen))

		elemType := ast.Type{Name: t.Name}

		if _, ok := g.structs[t.Name]; ok {
			for i := 0; i < t.ArrayLen; i++ {
				g.emitStaticValue(elemType)
			}
			return
		}

		g.emit(fmt.Sprintf("    .fill %d, 0", g.sizeof(elemType)*t.ArrayLen))
		return
	}

	if s, ok := g.structs[t.Name]; ok {
		for _, f := range s.Fields {
			g.emitStaticValue(f.Type)
		}
		return
	}

	g.emit(fmt.Sprintf("    .fill %d, 0", g.sizeof(t)))
}

func (g *Generator) emit(s string) {
	g.out.WriteString(s)
	g.out.WriteString("\n")
}
