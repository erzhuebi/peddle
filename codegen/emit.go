func (g *Generator) emitHeader() {
	g.emit(`; generated by peddlec

ZP_PTR1_LO  = $f9
ZP_PTR1_HI  = $fa
ZP_TMP0     = $fb
ZP_TMP1     = $fc
ZP_PTR0_LO  = $fd
ZP_PTR0_HI  = $fe

* = $0801
.word basic_next
.word 10
.byte $9e
.text "2064"
.byte 0

basic_next:
.word 0

* = $0810

start:
    ; Disable BASIC ROM, keep KERNAL ROM and I/O visible.
    lda #$36
    sta $01

    jsr main

    ; Restore normal C64 banking before returning to BASIC.
    ; BASIC ROM on, KERNAL ROM on, I/O visible.
    lda #$37
    sta $01

    rts
`)
}

func (g *Generator) emitRuntime() {
	g.emit("")
	g.emit("; runtime")

	g.emitRuntimeVariables()

	g.emitPrintRuntime()
	g.emitArrayCopyRuntime()
	g.emitFillRuntime()
	g.emitAppendRuntime()
	g.emitMulRuntime()
	g.emitDivModRuntime()
	g.emitShiftRuntime()
	g.emitStringRuntime()
	g.emitClsRuntime()
	g.emitAsciiFontRuntime()
	g.emitAsciiConvertRuntime()
	g.emitItoaRuntime()
	g.emitItoxRuntime()
	g.emitCharToScreenTable()
	g.emitReadLineRuntime()
	g.emitPutStrRuntime()
	g.emitNetRuntime()
	g.emitFileRuntime()
}

func (g *Generator) emitRuntimeVariables() {
	needsTmp16 := g.usedTmp16 ||
		g.usedPrint ||
		g.usedArrayCopyRuntime ||
		g.usedFillByteRuntime ||
		g.usedFillIntRuntime ||
		g.usedAppendByteRuntime ||
		g.usedAppendIntRuntime ||
		g.usedMulByteRuntime ||
		g.usedMulIntRuntime ||
		g.usedDivModByteRuntime ||
		g.usedDivModIntRuntime ||
		g.usedShlByteRuntime ||
		g.usedShrByteRuntime ||
		g.usedShlIntRuntime ||
		g.usedShrIntRuntime ||
		g.usedStringCopyRuntime ||
		g.usedStringAppendRuntime ||
		g.usedPutStrRuntime ||
		g.usedPutStrColorRuntime ||
		g.usedItoaRuntime

	if needsTmp16 {
		g.emit("peddle_tmp_int0:")
		g.emit("    .fill 2, 0")
	}
}

func (g *Generator) emitItoaRuntime() {
	if !g.usedItoaRuntime {
		return
	}

	g.emit(`
; itoa runtime
peddle_itoa_buffer:
    .word 6
    .word 0
    .fill 6, 0

peddle_itoa_started:
    .byte 0
peddle_itoa_digit:
    .byte 0
peddle_itoa_div_lo:
    .byte 0
peddle_itoa_div_hi:
    .byte 0
peddle_itoa_force:
    .byte 0

peddle_itoa:
    lda #0
    sta peddle_itoa_buffer+2
    sta peddle_itoa_buffer+3
    sta peddle_itoa_started

    lda ZP_TMP1
    and #$80
    beq peddle_itoa_positive

    lda #45
    ldy #0
    sta peddle_itoa_buffer+4, y
    lda #1
    sta peddle_itoa_buffer+2

    lda ZP_TMP0
    eor #$ff
    clc
    adc #1
    sta ZP_TMP0

    lda ZP_TMP1
    eor #$ff
    adc #0
    sta ZP_TMP1

peddle_itoa_positive:
    lda #<10000
    sta peddle_itoa_div_lo
    lda #>10000
    sta peddle_itoa_div_hi
    lda #0
    sta peddle_itoa_force
    jsr peddle_itoa_emit_digit

    lda #<1000
    sta peddle_itoa_div_lo
    lda #>1000
    sta peddle_itoa_div_hi
    lda #0
    sta peddle_itoa_force
    jsr peddle_itoa_emit_digit

    lda #<100
    sta peddle_itoa_div_lo
    lda #>100
    sta peddle_itoa_div_hi
    lda #0
    sta peddle_itoa_force
    jsr peddle_itoa_emit_digit

    lda #<10
    sta peddle_itoa_div_lo
    lda #>10
    sta peddle_itoa_div_hi
    lda #0
    sta peddle_itoa_force
    jsr peddle_itoa_emit_digit

    lda #<1
    sta peddle_itoa_div_lo
    lda #>1
    sta peddle_itoa_div_hi
    lda #1
    sta peddle_itoa_force
    jsr peddle_itoa_emit_digit

    rts

peddle_itoa_emit_digit:
    lda #0
    sta peddle_itoa_digit

peddle_itoa_digit_loop:
    lda ZP_TMP1
    cmp peddle_itoa_div_hi
    bcc peddle_itoa_digit_done
    bne peddle_itoa_digit_subtract

    lda ZP_TMP0
    cmp peddle_itoa_div_lo
    bcc peddle_itoa_digit_done

peddle_itoa_digit_subtract:
    sec
    lda ZP_TMP0
    sbc peddle_itoa_div_lo
    sta ZP_TMP0
    lda ZP_TMP1
    sbc peddle_itoa_div_hi
    sta ZP_TMP1

    inc peddle_itoa_digit
    jmp peddle_itoa_digit_loop

peddle_itoa_digit_done:
    lda peddle_itoa_force
    bne peddle_itoa_digit_emit

    lda peddle_itoa_started
    bne peddle_itoa_digit_emit

    lda peddle_itoa_digit
    bne peddle_itoa_digit_start

    rts

peddle_itoa_digit_start:
    lda #1
    sta peddle_itoa_started

peddle_itoa_digit_emit:
    lda peddle_itoa_digit
    clc
    adc #48

    ldy peddle_itoa_buffer+2
    sta peddle_itoa_buffer+4, y

    inc peddle_itoa_buffer+2
    rts
`)
}

